def detect_steps(data, fs=50):
    mag = np.sqrt(data['accel_x']**2 + data['accel_y']**2 + data['accel_z']**2)
    b, a = butter(4, [0.5, 3.0], btype='band', fs=fs)
    mag_filt = filtfilt(b, a, mag - mag.mean())

    # Adaptive threshold: only count peaks that stand out from typical noise
    threshold = mag_filt.std() * 1.0   # tune 1.0 → 1.5 if still over-counting

    peaks, _ = find_peaks(
        mag_filt,
        height=threshold,
        distance=int(0.3 * fs),   # min 0.3 s between steps
        prominence=threshold * 0.5,  # peak must "stick up" from local baseline
    )
    return peaks, mag_filt

