def add_magnitude(data, magnitude_groups):
    """
    Append magnitude channels to the dataframe.

    magnitude_groups: {new_col_name: [axis_columns]}
        e.g. {'accel_mag': ['accel_x', 'accel_y', 'accel_z']}
    """
    data = data.copy()
    for new_col, axes in magnitude_groups.items():
        data[new_col] = np.sqrt(np.sum(data[axes].values ** 2, axis=1))
    return data


def raw_to_windows(data, window_size, step, sample_rate,
                   freq_bands=((0.5, 3.0), (3.0, 6.0))):
    """
    Slide a window over `data` and compute time- and frequency-domain features per column.

    sample_rate: Hz of the input signal (needed to map FFT bins to Hz)
    freq_bands:  list of (low, high) Hz pairs for spectral energy features
    """
    rows = []
    cols = list(data.columns)
    freqs = np.fft.rfftfreq(window_size, d=1.0 / sample_rate)

    for start in range(0, len(data) - window_size + 1, step):
        window = data.iloc[start:start + window_size]
        feats = {'window_start': start}

        for col in cols:
            x = window[col].values

            # Time-domain
            feats[f'{col}_mean']  = x.mean()
            feats[f'{col}_std']   = x.std()
            feats[f'{col}_min']   = x.min()
            feats[f'{col}_max']   = x.max()
            feats[f'{col}_range'] = x.max() - x.min()
            feats[f'{col}_rms']   = np.sqrt(np.mean(x ** 2))

            # Frequency-domain (subtract mean to drop DC)
            spec = np.abs(np.fft.rfft(x - x.mean()))

            # Dominant frequency (skip DC bin)
            dom_bin = 1 + np.argmax(spec[1:]) if len(spec) > 1 else 0
            feats[f'{col}_dom_freq'] = freqs[dom_bin]

            # Spectral energy per band
            for low, high in freq_bands:
                mask = (freqs >= low) & (freqs < high)
                feats[f'{col}_energy_{low}-{high}Hz'] = np.sum(spec[mask] ** 2)

        rows.append(feats)

    return pd.DataFrame(rows)

def raw_to_window(data, window_size, overlap):
  window_start = 0
  return_data = pd.DataFrame()
  # Create window
  while window_start <= data.shape[0] - window_size:
    window_end = window_start + window_size
    window_end = window_start + window_size
    window = data.iloc[window_start:window_end, :]
    # For each value (accel/gyro x,y,z), calculate the terms
    return_for_window = pd.DataFrame()
    for col in window.columns:
      return_for_window.loc[0, col + '_mean'] = window[col].mean()
      return_for_window.loc[0,col + '_std'] = window[col].std()
      return_for_window.loc[0,col + '_min'] = window[col].min()
      return_for_window.loc[0,col + '_max'] = window[col].max()
      return_for_window.loc[0,col + '_range'] = window[col].max() - window[col].min()
      return_for_window.loc[0,col + '_rms'] = np.sqrt(np.mean(np.square(window[col])))
    return_data = pd.concat([return_data, return_for_window])
    window_start += overlap
  return return_data

