def gait_similarity(peaks, fs=50):
    """
    Score 1-10 measuring how regular the user's gait is.
    Higher = more consistent stride timing = lower fall risk.

    Built from:
      - CV of step intervals (timing consistency)
      - Asymmetry between left/right step intervals (limp detection)
    """
    if len(peaks) < 4:
        return None, "Not enough steps to assess gait similarity"

    # Step intervals in seconds
    intervals = np.diff(peaks) / fs

    # 1. Coefficient of variation: lower = more regular
    cv = intervals.std() / intervals.mean()

    # 2. Asymmetry: in healthy gait, odd & even step intervals are similar
    #    (left-right-left-right gives roughly equal alternating intervals).
    #    A limp creates a long-short-long-short pattern.
    odd_intervals = intervals[::2]
    even_intervals = intervals[1::2]
    n = min(len(odd_intervals), len(even_intervals))
    if n >= 2:
        asymmetry = abs(odd_intervals[:n].mean() - even_intervals[:n].mean()) \
                    / intervals.mean()
    else:
        asymmetry = 0.0

    # 3. Map to a 1-10 score.
    # Healthy reference values from the literature:
    #   CV < 0.05 is excellent, > 0.15 is concerning
    #   Asymmetry < 0.05 is excellent, > 0.20 is concerning
    cv_score        = np.clip(1 - (cv - 0.03) / 0.17, 0, 1)        # 1 at cv=0.03, 0 at cv=0.20
    asym_score      = np.clip(1 - (asymmetry - 0.02) / 0.18, 0, 1) # 1 at asym=0.02, 0 at asym=0.20

    # Weighted combo — both matter, asymmetry slightly more for fall risk
    combined = 0.45 * cv_score + 0.55 * asym_score
    score_1_to_10 = 1 + 9 * combined   # rescale to 1-10

    diagnostics = {
        'cv': float(cv),
        'asymmetry': float(asymmetry),
        'mean_interval_s': float(intervals.mean()),
        'n_steps': int(len(peaks)),
    }
    return float(score_1_to_10), diagnostics


