def detect_stance_phases(data, fs=50, accel_var_thresh=None, gyro_mag_thresh=None):
    """
    Stance phase = foot is flat on ground = low gyro magnitude AND low accel variance.
    Returns a boolean array, True where the foot is in stance.
    """
    gyro_mag = np.sqrt(data['gyro_x']**2 + data['gyro_y']**2 + data['gyro_z']**2)
    accel_mag = np.sqrt(data['accel_x']**2 + data['accel_y']**2 + data['accel_z']**2)

    # Rolling variance of accel magnitude over a small window (~50 ms)
    win = max(int(0.05 * fs), 3)
    accel_var = pd.Series(accel_mag).rolling(win, center=True).var().fillna(0).values

    # Adaptive thresholds if not supplied
    if gyro_mag_thresh is None:
        gyro_mag_thresh = np.percentile(gyro_mag, 30)
    if accel_var_thresh is None:
        accel_var_thresh = np.percentile(accel_var, 30)

    stance = (gyro_mag < gyro_mag_thresh) & (accel_var < accel_var_thresh)
    return stance


def estimate_foot_clearance(data, fs=50, gravity_raw=16384):
    """
    Estimate per-step foot clearance using ZUPT.

    `gravity_raw`: raw accel value corresponding to 1 g. For MPU6050 at ±2g range,
    this is 16384. Adjust if your sensor is configured differently.

    Returns: list of clearance heights (in meters), one per stride.
    """
    # 1. Convert raw accel to m/s²  (raw → g → m/s²)
    g = 9.81
    ax = data['accel_x'].values / gravity_raw * g
    ay = data['accel_y'].values / gravity_raw * g
    az = data['accel_z'].values / gravity_raw * g

    # 2. Acceleration magnitude minus gravity. We're not doing full orientation
    # tracking here, so we approximate vertical accel as (|a| - g).
    # For better accuracy, integrate gyro to track orientation and rotate the
    # accel vector into the world frame. This simple version works decently for
    # foot-mounted sensors where the foot is roughly level during swing.
    a_mag = np.sqrt(ax**2 + ay**2 + az**2)
    a_vert = a_mag - g

    # 3. Find stance phases
    stance = detect_stance_phases(data, fs=fs)

    # 4. Integrate acceleration → velocity → position, resetting at each stance
    dt = 1.0 / fs
    velocity = np.zeros(len(a_vert))
    position = np.zeros(len(a_vert))

    clearances = []
    in_swing = False
    swing_start = 0

    for i in range(1, len(a_vert)):
        if stance[i]:
            # Foot on ground — reset velocity, mark end of swing if we were in one
            if in_swing:
                # Find max height during the swing we just finished
                swing_height = position[swing_start:i].max() - position[swing_start]
                clearances.append(swing_height)
                in_swing = False
            velocity[i] = 0.0
            position[i] = position[i - 1]   # don't drift while planted
        else:
            # Swing phase — integrate
            if not in_swing:
                in_swing = True
                swing_start = i
                position[i] = position[i - 1]   # start swing from last stance height
            velocity[i] = velocity[i - 1] + a_vert[i] * dt
            position[i] = position[i - 1] + velocity[i] * dt

    return np.array(clearances)


